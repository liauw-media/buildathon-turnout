#!/usr/bin/env node
// scripts/build-world-geo.cjs
// Reads /tmp/world.geo.json, projects to equirectangular 1000x500,
// and writes lib/world-geo.ts
// Run: node scripts/build-world-geo.cjs

"use strict";

const fs = require("fs");
const path = require("path");

const INPUT = "/tmp/world.geo.json";
const OUTPUT = path.join(__dirname, "../lib/world-geo.ts");

// Projection bounds — world, dropping extreme south (Antarctica)
const VIEW_W = 1000;
const VIEW_H = 500;
const PAD = 0; // no padding — full bleed world map
const BOUNDS = { minLat: -60, maxLat: 85, minLng: -180, maxLng: 180 };

function project(lng, lat) {
  const x =
    PAD +
    ((lng - BOUNDS.minLng) / (BOUNDS.maxLng - BOUNDS.minLng)) * (VIEW_W - 2 * PAD);
  const y =
    PAD +
    ((BOUNDS.maxLat - lat) / (BOUNDS.maxLat - BOUNDS.minLat)) * (VIEW_H - 2 * PAD);
  return [x, y];
}

function r1(n) {
  return Math.round(n * 10) / 10;
}

function ringToPath(ring) {
  let d = "";
  for (let i = 0; i < ring.length; i++) {
    const [lng, lat] = ring[i];
    // skip out-of-bounds points (Antarctica etc.)
    if (lat < BOUNDS.minLat || lat > BOUNDS.maxLat) continue;
    const [x, y] = project(lng, lat);
    d += (i === 0 || d === "" ? "M" : "L") + r1(x) + "," + r1(y);
  }
  if (d) d += "Z";
  return d;
}

function polygonArea(ring) {
  // Shoelace formula for approximate area (in lng/lat degrees² space)
  let area = 0;
  for (let i = 0, j = ring.length - 1; i < ring.length; j = i++) {
    area += (ring[j][0] + ring[i][0]) * (ring[j][1] - ring[i][1]);
  }
  return Math.abs(area / 2);
}

function featureToPath(feature) {
  const geom = feature.geometry;
  const props = feature.properties;
  const name = props.NAME || props.name || props.ADMIN || "Unknown";
  const iso = props.ISO_A2 || props.ADM0_A3 || props.ISO_A3 || "-99";

  if (!geom) return null;

  let polygons = [];

  if (geom.type === "Polygon") {
    polygons = [geom.coordinates];
  } else if (geom.type === "MultiPolygon") {
    polygons = geom.coordinates;
  } else {
    return null;
  }

  // Find the largest polygon by area to determine if we should include this country
  let maxArea = 0;
  let paths = [];

  for (const polygon of polygons) {
    const outerRing = polygon[0];
    const area = polygonArea(outerRing);
    if (area > maxArea) maxArea = area;

    // Build path for outer ring + holes
    let pd = ringToPath(outerRing);
    if (!pd) continue;
    // Add holes
    for (let h = 1; h < polygon.length; h++) {
      pd += ringToPath(polygon[h]);
    }
    if (pd && pd.length > 1) {
      paths.push({ pd, area });
    }
  }

  // Skip microstates — largest polygon area < 0.5 deg² (very tiny on the world map)
  if (maxArea < 0.5) return null;

  // Sort paths by area descending (biggest polygon first) — skip tiny slivers
  paths.sort((a, b) => b.area - a.area);

  // Only keep paths with area > 1% of the largest polygon to avoid tiny slivers
  const areaThreshold = maxArea * 0.01;
  const filteredPaths = paths.filter((p) => p.area >= areaThreshold || p.area >= 0.5);

  const d = filteredPaths.map((p) => p.pd).join(" ");
  if (!d) return null;

  return { name, iso, d };
}

console.log("Reading", INPUT, "...");
const raw = fs.readFileSync(INPUT, "utf8");
const geojson = JSON.parse(raw);

console.log("Total features:", geojson.features.length);

const paths = [];
let skipped = 0;

for (const feature of geojson.features) {
  const result = featureToPath(feature);
  if (result) {
    paths.push(result);
  } else {
    skipped++;
  }
}

console.log(`Included: ${paths.length}, Skipped (microstates/tiny): ${skipped}`);

// Escape any backticks or template literal chars in path data (shouldn't happen but safe)
const json = JSON.stringify(paths, null, 0);

const ts = `// AUTO-GENERATED by scripts/build-world-geo.cjs — do not edit by hand.
// Source: Natural Earth 110m world countries, equirectangular projection.
// Projection: lat -60..85, lng -180..180 → 1000×500 viewBox.
export type CountryPath = { name: string; iso: string; d: string };

export const WORLD_PATHS: CountryPath[] = ${json};

export const WORLD_VIEW = { w: 1000, h: 500 };

export const WORLD_BOUNDS = { minLat: -60, maxLat: 85, minLng: -180, maxLng: 180 };
`;

fs.writeFileSync(OUTPUT, ts, "utf8");
const size = fs.statSync(OUTPUT).size;
console.log(`Written ${OUTPUT} — ${(size / 1024).toFixed(1)} KB`);
